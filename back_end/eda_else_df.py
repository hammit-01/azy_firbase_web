import numpy as np
import pandas as pd
from back_end.eda_standard import eda_standard


# =========================================================
# 강동
# =========================================================
def kd_eda(data):
    if data is None or data.empty:
        return

    df = data.drop_duplicates().copy()

    # () 제거
    df["기타정보"] = (
        df["기타정보"]
        .astype(str)
        .str.replace("()", "", regex=False)
    )

    # -------------------------------------------------
    # 등급
    # -------------------------------------------------
    df["등급"] = (
        df["기타정보"]
        .astype(str)
        .str.extract(r"^([^/]+)")[0]
    )

    # / 시작이면 등급 없음
    df.loc[
        df["기타정보"].astype(str).str.startswith("/"),
        "등급"
    ] = None

    # -------------------------------------------------
    # 브랜드
    # WINGHAM15.57KG -> WINGHAM
    # SADIA/BRF12KG -> SADIA
    # -------------------------------------------------
    df["브랜드"] = (
        df["규격단위중량"]
        .astype(str)
        .str.extract(r"^([A-Za-z/]+)")[0]
        .str.split("/")
        .str[0]
    )

    # 평균중량
    df["평균중량"] = (
        df["규격단위중량"]
        .astype(str)
        .str.extract(r"(\d+(?:\.\d+)?)")[0]
        .astype(float)
    )

    # -------------------------------------------------
    # 기타정보 제거
    # -------------------------------------------------
    df = df.drop(
        columns=["기타정보"],
        errors="ignore"
    )

    return df


# =========================================================
# 경인 / 삼진
# =========================================================
def ki_eda(data1):
    if data1 is None or data1.empty:
        return data1

    ki = data1.drop_duplicates().copy()

    # =====================================================
    # 1. 경인 (ki)
    # =====================================================
    d = ki.copy()

    s = d["기타정보"].astype(str)

    # -------------------------------------------------
    # 등급
    # A(SOUTHERN)180 -> A
    # -------------------------------------------------
    d["등급"] = s.str.extract(r"^([A-Za-z])")[0]

    # -------------------------------------------------
    # 브랜드
    # (SOUTHERN)
    # -------------------------------------------------
    d["브랜드"] = s.str.extract(r"\((.*?)\)")[0]

    # -------------------------------------------------
    # ESTNO
    # 마지막 숫자
    # -------------------------------------------------
    d["ESTNO"] = s.str.extract(r"(\d+[A-Za-z]*)$")[0]

    # 평균중량
    s = (
        d["규격단위중량"]
        .astype(str)
        .str.replace(r"\(.*?\)", "", regex=True)
    )

    d["평균중량"] = (
        s.str.extract(r"^(.*?)(?=[A-Z])")[0]
        .astype(float)
    )

    # -------------------------------------------------
    # 기타정보 제거
    # -------------------------------------------------
    ki = d.drop(
        columns=["기타정보"],
        errors="ignore"
    )

    return ki

def sjn_eda(data1):
    if data1 is None or data1.empty:
            return

    sjn = data1.drop_duplicates().copy()
    # =====================================================
    # 2. 삼진 (sjn)
    # =====================================================
    d2 = sjn.copy()

    s2 = d2["기타정보"].astype(str)

    # -------------------------------------------------
    # 전처리
    # ★ 제거
    # -------------------------------------------------
    s2 = (
        s2.str.replace("★", "", regex=False)
          .str.replace("☆", "", regex=False)
    )

    # -------------------------------------------------
    # 브랜드
    # ()(EXCEL)CH#86M -> EXCEL
    # -------------------------------------------------
    d2["브랜드"] = (
        s2.str.extract(r"\(([^()]+)\)")[0]
    )

    # -------------------------------------------------
    # 등급
    # ()(TEYS)UN_294 -> UN
    # -------------------------------------------------
    d2["등급"] = s2.str.extract(
        r"\)\s*([A-Za-z]+)"
    )[0]
    
    # -------------------------------------------------
    # ESTNO
    # _294 -> 294
    # -------------------------------------------------
    d2["ESTNO"] = (
        d2["기타정보"]
        .astype(str)
        .str.extract(r"[)_]([^)_]+)$")[0]
    )

    # 평균중량
    s = (
        d2["규격단위중량"]
        .astype(str)
        .str.replace(r"\(.*?\)", "", regex=True)
    )

    d2["평균중량"] = (
        s.str.extract(r"^(.*?)(?=[A-Z])")[0]
        .astype(float)
    )

    # -------------------------------------------------
    # 기타정보 제거
    # -------------------------------------------------
    sjn = d2.drop(
        columns=["기타정보"],
        errors="ignore"
    )

    return sjn


# =========================================================
# 대청
# =========================================================
def dch_eda(data):
    if data is None or data.empty:
        return

    dch = data.drop_duplicates().copy()

    s = dch["기타정보"].fillna("").astype(str)

    # -----------------------------------
    # 1. 특수문자 제거
    # -----------------------------------
    s = s.str.replace(r"[♥☆★\(\)]", "", regex=True)

    # -----------------------------------
    # 2. 브랜드 추출 (기본)
    # -----------------------------------
    brand = s.str.extract(r"^([A-Z]+)")[0]

    # -----------------------------------
    # 3. EXUN 예외 처리
    # -----------------------------------
    mask_exun = s.str.startswith("EXUN")

    brand = brand.where(~mask_exun, "EX")
    dch.loc[mask_exun, "등급"] = "UN"

    # -----------------------------------
    # 4. 브랜드 저장
    # -----------------------------------
    dch["브랜드"] = brand

    # -----------------------------------
    # 5. 평균중량 (유지)
    # -----------------------------------
    w = (
        dch["규격단위중량"]
        .fillna("")
        .astype(str)
        .str.replace(r"\(.*?\)", "", regex=True)
    )

    dch["평균중량"] = (
        w.str.extract(r"(\d+(?:\.\d+)?)")[0]
        .astype(float)
    )

    # -----------------------------------
    # 6. 기본 등급 (나머지)
    # -----------------------------------
    if "등급" not in dch.columns:
        dch["등급"] = None

    return dch


# =========================================================
# 한라 곤지암 / 동탄
# =========================================================
def hl_eda(data1):

    if data1 is None or data1.empty:
        return data1

    hl = data1.drop_duplicates().copy()

    # =================================================
    # 평균중량
    # =================================================
    hl["평균중량"] = (
        hl["규격단위중량"]
        .astype(str)
        .str.extract(r"(\d+\.?\d*)")[0]
    )

    hl["평균중량"] = pd.to_numeric(
        hl["평균중량"],
        errors="coerce"
    )

    # =================================================
    # 기타정보
    # =================================================
    s = hl["기타정보"].astype(str)

    # 앞 괄호 제거
    s = s.str.replace(
        r"^\(.*?\)",
        "",
        regex=True
    )

    # =================================================
    # / 기준 분리
    # =================================================
    tmp = s.str.split(
        "/",
        n=2,
        expand=True
    )

    tmp = tmp.reindex(columns=[0, 1, 2])

    hl["등급"] = tmp[0]
    hl["ESTNO"] = tmp[1]
    hl["브랜드"] = tmp[2]

    # =================================================
    # 정리
    # =================================================
    for col in ["등급", "ESTNO", "브랜드"]:

        hl[col] = (
            hl[col]
            .fillna("")
            .astype(str)
            .str.replace(
                r"\(.*?\)",
                "",
                regex=True
            )
            .str.strip()
        )

    # =================================================
    # 브랜드만 있는 경우
    # =================================================
    mask = ~s.str.contains("/")

    hl.loc[mask, "브랜드"] = (
        s[mask]
        .str.replace(
            r"\d+\.?\d*$",
            "",
            regex=True
        )
        .str.strip()
    )

    hl.loc[
        mask,
        ["등급", "ESTNO"]
    ] = None

    hl = hl.drop(
        columns=["기타정보"],
        errors="ignore"
    )

    return hl


# =========================================================
# 전체 통합
# =========================================================
def else_df_eda(
    kd,
    ki,
    sjn,
    dch,
    hlk,
    hld
):

    # 강동
    kd = kd_eda(kd)

    # 경인 / 삼진
    ki = ki_eda(ki)
    sjn = sjn_eda(sjn)

    # 대청
    dch = dch_eda(dch)

    # 한라
    hlk = hl_eda(hlk)
    hld = hl_eda(hld)

    # =====================================================
    # 병합
    # =====================================================
    dfs = [kd, ki, sjn, dch, hlk, hld]
    valid_dfs = [
        d for d in dfs
        if d is not None and not d.empty
    ]

    df = pd.concat(
        valid_dfs,
        ignore_index=True
    )

    # =====================================================
    # 공통 전처리
    # =====================================================
    df = eda_standard(df)

    # =====================================================
    # 불필요 컬럼 제거
    # =====================================================
    df = df.drop(
        columns=["규격단위중량", "기타정보"],
        errors="ignore"
    )

    return df