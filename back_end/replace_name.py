def replace_name(df):
    for _col in ("수탁품", "상품명"):
        if _col in df.columns:
            df[_col] = df[_col].astype(str).str.strip().replace(PRODUCT_NAME_MAP)
    df["브랜드"] = (
        df["브랜드"]
        .astype(str)
        .str.replace(' ', ' ', regex=False)  # non-breaking space → 일반 공백
        .str.replace(r'\s+', ' ', regex=True)     # 연속 공백 정리
        .str.replace('_', '', regex=False)   # 언더스코어 제거
        .str.strip()
        .replace({"nan": "", "None": ""})
    )
    df["브랜드"] = df["브랜드"].replace(BRAND_MAP)
    df["등급"] = df["등급"].replace(GRADE_MAP)
    df["ESTNO"] = df["ESTNO"].replace(ESTNO_MAP)
    df["ESTNO"] = (
        df["ESTNO"].fillna("").astype(str)
        .str.replace("-", "", regex=False)
        .replace({"nan": "", "None": ""})
    )

    name_col = "수탁품" if "수탁품" in df.columns else ("상품명" if "상품명" in df.columns else None)
    if name_col:
        for rule in FIELD_OVERRIDE_RULES:
            mask = pd.Series(True, index=df.index)
            for col, val in rule["match"].items():
                check_col = name_col if col == "상품명" else col
                mask &= (df[check_col] == val)
            for col, val in rule["set"].items():
                set_col = name_col if col == "상품명" else col
                df.loc[mask, set_col] = val

    return df
